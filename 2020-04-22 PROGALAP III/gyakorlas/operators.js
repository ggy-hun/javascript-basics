const sum = (a, b, c) => {
    return a + b + c;
};

const sub = (a, b) => {
    return a - b;
};

const mul = (a, b) => {
    return a * b;
};

const div = (a, b) => {
    return a / b;
};

module.exports = {              
    sum,
    sub,
    mul,                        
    div,                        
};